package gov.nasa.pds.entities;

/**
 * This enumeration represents the enumeration of possible types of items in the metadata.
 * 
 * Thread Safety: This class is immutable and thread safe.
 */
public enum ItemType {
    /**
     * Represents an item type of an object.
     */
    OBJECT,
    /**
     * Represents an item type of a property.
     */
    PROPERTY;
}
